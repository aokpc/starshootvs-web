export function p2m(password: string) {
    let str = "";
    password = decodeXYZ(password);
    for (let i = 0; i < password.length; i++) {
        let s = password.substr(i, 1);
        let num = parseInt(s, 16);
        let bit = toBit(num, 4);
        str += bit;
    }
    return toMap(str);
}

function toMap(str: string) {
    str = checkSkin(str)
    const map: number[][] = [];
    for (let j = 0; j < 16; j++) {
        map[j] = [];
        for (let i = 16 * j; i < (16 * j + 16); i++) {
            if (str.length < i) {
                map[j][i % 16] = 0;
                continue;
            }
            map[j][i % 16] = parseInt(str[i])
        }
    }
    return map;
}
function checkSkin(bits: string) {
    var countPixel = 0
    bits.split("").forEach((e) => {
        if (e === "1") {
            countPixel += 1
        }
    })
    if (countPixel > 16 * 2) {
        return bits
    }
    return "1".repeat(16 * 16)
}
function toBit(num: number, size: number) {
    let s = num.toString(2)
    for (let i = 0; i < 999; i++) {
        if (s.length >= size) {
            return s;
        }
        s = "0" + s;
    }
    return s;
}

function decodeXYZ(str: string) {
    str = str.replace(/z/g, "00000000");
    str = str.replace(/y/g, "0000");
    str = str.replace(/x/g, "00");
    str = str.replace(/Z/g, "ffffffff");
    str = str.replace(/Y/g, "ffff");
    str = str.replace(/X/g, "ff");
    return str;
}
